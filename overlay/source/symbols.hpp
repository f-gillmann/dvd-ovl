#include <switch.h>

class Symbol
{
	private:
		s32 s_width;
		s32 s_height;
		const u8 *s_data;

	public:
		constexpr Symbol(size_t width, size_t height, const u8 *data)
			: s_width(width), s_height(height), s_data(data) { }

		void draw(s32 x, s32 y, tsl::gfx::Renderer *renderer) const
		{
			const u8 *ptr = this->s_data;
			const s32 x_offset = x - (s_width / 2);
			const s32 y_offset = y - (s_height / 2);
			tsl::Color color = tsl::style::color::ColorText; 

			for (s32 y1 = 0; y1 < s_height; y1++) {
				for (s32 x1 = 0; x1 < s_width; x1++) {
					color.a = static_cast<u16>(ptr[0] >> 4);
					renderer->setPixelBlendSrc(x_offset + x1, y_offset + y1, renderer->a(color));
					ptr++;
				}
			}
		}
};

